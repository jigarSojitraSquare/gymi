textWith16W400(Color color) {
  return TextStyle(
    color: color,
    fontSize: 16.sp,
    fontWeight: FontWeight.w400,
  );
}

textWith48W600(Color color) {
  return TextStyle(
    color: color,
    fontSize: 48.sp,
    fontWeight: FontWeight.w600,
  );
}

